package rs.ac.bg.fon.nst.fitnes.domain;


import jakarta.persistence.*;
import lombok.Data;
import lombok.NoArgsConstructor;
import lombok.AllArgsConstructor;
import java.time.LocalDate;
import java.time.LocalDateTime;

@Entity
@Table(name = "planovi_vezbi")
@Data
@NoArgsConstructor
@AllArgsConstructor
public class PlanVezbe {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @ManyToOne(fetch = FetchType.LAZY)
    @JoinColumn(name = "plan_treninga_id", nullable = false)
    private PlanTreninga planTreninga;

    @ManyToOne(fetch = FetchType.LAZY)
    @JoinColumn(name = "vezba_id", nullable = false)
    private Vezba vezba;

    private Integer brojSerija;

    private Integer brojPonavljanja;

    private LocalDate datum; 

    @Column(name = "created_at", updatable = false)
    private LocalDateTime createdAt;

    @Column(name = "updated_at")
    private LocalDateTime updatedAt;

    @PrePersist
    protected void onCreate() {
        createdAt = LocalDateTime.now();
        updatedAt = LocalDateTime.now();
    }

    @PreUpdate
    protected void onUpdate() {
        updatedAt = LocalDateTime.now();
    }
}
